package models;

public class Line
{
	private Double x;
	private Double y;
	private Double dx;
	private Double dy;

	public Double getX()
	{
		return x;
	}

	public void setX(Double x)
	{
		this.x = x;
	}

	public Double getY()
	{
		return y;
	}

	public void setY(Double y)
	{
		this.y = y;
	}

	public Double getDx()
	{
		return dx;
	}

	public void setDx(Double dx)
	{
		this.dx = dx;
	}

	public Double getDy()
	{
		return dy;
	}

	public void setDy(Double dy)
	{
		this.dy = dy;
	}
}
