package models;

public class Line
{
	private Long x;
	private Long y;
	private Long dx;
	private Long dy;

	public Long getX()
	{
		return x;
	}

	public void setX(Long x)
	{
		this.x = x;
	}

	public Long getY()
	{
		return y;
	}

	public void setY(Long y)
	{
		this.y = y;
	}

	public Long getDx()
	{
		return dx;
	}

	public void setDx(Long dx)
	{
		this.dx = dx;
	}

	public Long getDy()
	{
		return dy;
	}

	public void setDy(Long dy)
	{
		this.dy = dy;
	}
}
